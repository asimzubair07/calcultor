class CalcApp extends StatefulWidget {
  const CalcApp({Key key}) : super(key: key);

  @override
  CalcAppState createState() => CalcAppState();
}

class CalcAppState extends State<CalcApp> {
  String _history = '';
  String _expression = '';

  void numClick(String text) {
    setState(() => _expression += text);
  }

  void allClear(String text) {
    setState(() {
      _history = '';
      _expression = '';
    });
  }

  void squareroot(String text) {
    setState(() {
      _history = sqrt(int.parse(_expression)).toString();
    });
  }

  void exponentials(String text) {
    setState(() {
      _history = pies.exp(int.parse(_expression)).toString();
    });
  }

  void factorial(String text) {
    setState(() {
      _history = pies.acos(int.parse(_expression)).toString();
    });
  }

  void pie(String text) {
    setState(() {
      _expression += "3.142";
    });
  }

  void clear(String text) {
    setState(() {
      _expression = '';
    });
  }

  void evaluate(String text) {
    Parser p = Parser();
    Expression exp = p.parse(_expression);
    ContextModel cm = ContextModel();

    setState(() {
      _history = _expression;

      _expression = exp.evaluate(EvaluationType.REAL, cm).toString();
    });
  }

